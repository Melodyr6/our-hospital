package com.aaa.entity;

public class PrescriptionVo {

    private String cashier;
    private String doctorName;
    private String reportName;
    private String durgname;
    private String durgnum;
    private String repiceprice;
    private String repicetotal;

    public String getCashier() {
        return cashier;
    }

    public void setCashier(String cashier) {
        this.cashier = cashier;
    }

    public String getDoctorName() {
        return doctorName;
    }

    public void setDoctorName(String doctorName) {
        this.doctorName = doctorName;
    }

    public String getReportName() {
        return reportName;
    }

    public void setReportName(String reportName) {
        this.reportName = reportName;
    }

    public String getDurgname() {
        return durgname;
    }

    public void setDurgname(String durgname) {
        this.durgname = durgname;
    }

    public String getDurgnum() {
        return durgnum;
    }

    public void setDurgnum(String durgnum) {
        this.durgnum = durgnum;
    }

    public String getRepiceprice() {
        return repiceprice;
    }

    public void setRepiceprice(String repiceprice) {
        this.repiceprice = repiceprice;
    }

    public String getRepicetotal() {
        return repicetotal;
    }

    public void setRepicetotal(String repicetotal) {
        this.repicetotal = repicetotal;
    }
}
